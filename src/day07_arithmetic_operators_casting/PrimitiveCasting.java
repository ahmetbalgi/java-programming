package day07_arithmetic_operators_casting;

public class PrimitiveCasting {
    public static void main(String[] args) {
        int num1 = 100;
        byte b1 = (byte) num1;
        short num2 = (short) num1;
        long Lnum1 = num1;
        System.out.println(num1);
        System.out.println(b1);
        System.out.println(num2);
    }
}
